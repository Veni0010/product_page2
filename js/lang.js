// Сохраняем оригинальный HTML (а не текст!)
// Сохраняем оригинальные тексты для обратного восстановления
document.querySelectorAll('[data-translate]').forEach(el => {
  el.dataset.original = el.innerHTML.trim();
});

document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
  el.dataset.originalPlaceholder = el.placeholder.trim();
});

function setLanguage(lang) {
  localStorage.setItem('lang', lang);

  // Переводим обычные элементы с текстом
  document.querySelectorAll('[data-translate]').forEach(el => {
    const key = el.getAttribute('data-translate');
    const newText =
      translations[lang]?.[key] || el.dataset.original;

    el.classList.add('fade-out');
    setTimeout(() => {
      el.innerHTML = newText;
      el.classList.remove('fade-out');
    }, 400);
  });

  // Переводим placeholder'ы
  document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
    const key = el.getAttribute('data-translate-placeholder');
    const newPlaceholder =
      translations[lang]?.[key] || el.dataset.originalPlaceholder;

    // Эффект затухания при смене placeholder (опционально)
    el.classList.add('fade-out');
    setTimeout(() => {
      el.placeholder = newPlaceholder;
      el.classList.remove('fade-out');
    }, 400);
  });

  // Обновляем активный язык в переключателе
  const btn = document.querySelector('[data-lang="' + lang + '"]');
  if (btn) {
    document.getElementById('current-lang').innerHTML = btn.innerHTML;
  }
}

// --- Открытие / закрытие выпадающего списка ---
const switcher = document.querySelector('.lang-switcher');
const currentBtn = document.getElementById('current-lang');

currentBtn.addEventListener('click', () => {
  switcher.classList.toggle('open');
});

// Закрытие при клике вне
document.addEventListener('click', e => {
  if (!switcher.contains(e.target)) switcher.classList.remove('open');
});

// --- Обработка выбора языка ---
document.querySelectorAll('.lang-options [data-lang]').forEach(btn => {
  btn.addEventListener('click', () => {
    setLanguage(btn.dataset.lang);
    switcher.classList.remove('open');
  });
});

// --- Устанавливаем сохранённый язык ---
const savedLang = localStorage.getItem('lang') || 'ru';
setLanguage(savedLang);

